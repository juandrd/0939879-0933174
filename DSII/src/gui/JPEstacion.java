    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed
        int editar = -1;

        try {

            DirectoresEstacion director =controladorDirEstacion.consultar(jCBDirector3.getSelectedItem().toString());
            editar = controladorEstacion.modificar(
                    jTFNombre3.getText(),
                    jTFUbicacion3.getText(),                  
                    director);


        } catch (Exception e) {
            System.out.print(e);
        }
        if (editar == -1) {
            JOptionPane.showMessageDialog(this, "No su pudo modificar", "Error Base Datos", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "modificado correctamente", "Base Datos", JOptionPane.INFORMATION_MESSAGE);
            limpiarCamposConsultar();
            jTResultados.removeAll();
            jTFNombre2.setText(jTFNombre3.getText());
            jBConsultar1.doClick();
            jTPEstacion.setSelectedIndex(1);
            limpiarCamposModificar();
        }
    }//GEN-LAST:event_jBModificarActionPerformed

