    private void jBConsultar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBConsultar1ActionPerformed
        LinkedList consulta = new LinkedList();

        try {
           
            
            String director = jCBDirector2.getSelectedItem().toString();

            consulta = controladorEstacion.consultar(
                    jTFNombre2.getText(),
                    jTFUbicacion2.getText(),
                    director);

            Object[][] s = new Object[consulta.size()][5];
            for (int i = 0; i < consulta.size(); i++) {
                Estaciones e = (Estaciones) consulta.get(i);
                DirectoresEstacion directorEst=controladorDirEstacion.consultar(e.getDirector());
                Empleados empl=controladorEmpleados.consultar(directorEst.getIdentificacion());
                if (e.getNombre() != null) {
                    s[i][0] = e.getNombre();
                    s[i][1] = e.getUbicacion();
                    s[i][2] = e.getDirector();
                    s[i][3] = empl.getNombres();
                    s[i][4] = empl.getApellidos();
                   

                } else {
                    s = null;
                }
            }
            TableModel myModel = new DefaultTableModel(s, new String[]{
                        "Estacion", "Ubicacion", "Id-Director",
                        "Nombre Dir", "Apellido Dir"}) {

                boolean[] canEdit = new boolean[]{false, false, false, false, false};

                @Override
                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit[columnIndex];
                }
            };
            ///remover filas
            jTResultados.setModel(myModel);
            jTResultados.setRowSorter(new TableRowSorter(myModel));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jBConsultar1ActionPerformed

